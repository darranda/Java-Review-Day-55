package com.codingbat.string1;

public class HelloName {

    /**
     * Given a string name, e.g. "Bob", return a greeting of the form "Hello Bob!".
     */
    public String helloName(String name) {
        // form the greeting
        String helloName = "Hello " + name + "!";
        // return the greeting
        return helloName;
    }
}
